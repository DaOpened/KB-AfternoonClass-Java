package variable;

public class Variable1 {
    public static void main(String[] args) {
        int a = 3;
        double b = 3.14;
        float c = 3.141592f;
        boolean d = true;
        char e = 'p';
        String f = "Hello World";

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);

    }

}

